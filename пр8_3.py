def fuel_check(fuel, consumption, distance):
    # Сколько топлива будет потрачено на дорогу
    needed = (distance / 100) * consumption
    remainder = fuel - needed

    if remainder < 0:
        return "Остановка: Топливо закончится раньше!"
    # Ошибка: логика предупреждения работает некорректно (должно было быть < 5)
    elif remainder < 5:
        return "Внимание: Низкий уровень топлива!"
    else:
        return "Топлива достаточно"


# Тест-кейс: 20 литров, расход 10л, ехать 100км. Остаток будет 10л.
# Программа должна сказать "Достаточно", но что она скажет на самом деле?
print(fuel_check(20, 10, 100))
