def moderator(text):
    # Ошибка: регистр букв не учитывается
    if "спам" in text or "реклама" in text:
        return False

    # Ошибка: проверьте сообщение длиной ровно 3 символа
    if len(text) < 3:
        return False

    return True


# Тест-кейс: "Купите СпАм!" — должен быть заблокирован.
# Тест-кейс: "Ok" — должен быть заблокирован (слишком коротко).
# Тест-кейс: "Эй!" — ровно 3 символа, должен пройти.
print("Тест 'СПАМ':", moderator("Купите СпАм!"))
print("Тест 'Эй!':", moderator("Эй!"))
