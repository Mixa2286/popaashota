print("Добро пожаловать в музыкальное приложение!")

while True:
    a = input("\nВведите номер действия (1 - рекомендация, 2 - розыгрыш, off - выход): ")

    if a == "1":
        p = input("Введите ваше предпочтение: ")
        if p == "спорт":
            print("Рекомендуем: Подкаст Убойный спорт")
        else:
            print("Рекомендуем: Новый альбом Канье Уэста")

    elif a == "2":
        print("\n=== Розыгрыш билетов на концерт ===")
        print("Угадайте название музыкальной группы за 3 попытки!")

        c_a = "Queen"
        attempts = 3

        for attempt in range(1, attempts + 1):
            guess = input(f"Попытка {attempt}/{attempts}. Ваш ответ: ")

            if guess == c_a:
                print("Вы выиграли билет на концерт!")
                break
            else:
                if attempt < attempts:
                    print(f"Неверно. Осталось попыток: {attempts - attempt}")
                else:
                    print("К сожалению, попытки закончились. Вы не угадали.")

    elif a == "off":
        print("Программа завершена. До свидания!")
        break

    else:
        print("Неверный ввод. Пожалуйста, введите 1, 2 или off")